//数组名是什么  数组名就是首元素的地址.有两个例外
//例外1. sizeof(数组名)，计算整个数组的大小，sizeof内部单独放一个数组名，数组名表示整个数组。
//例外2. & 数组名，取出的是数组的地址。 & 数组名，数组名表示整个数组。
int main()
{
	int arr[] = { 1,2,3,4,5,6,7,8,9 };
	int sz = sizeof(arr) / sizeof(arr[0]);
	//连接例1  sizeof(数组名)表示的是整个数组，sizeof(数组名)，计算的是整个数组的大小，单位是字节
	//链接例2  &数组名，数组代表的是整个数组，&数组名，取出的是整个数组的地址 &arr

	printf("%p\n", arr);//首元素的地址就是这个数组名
	printf("%p\n", &arr[0]);//取首元素的地址
	printf("%d\n", *arr);//解引用     当前元素的值
	return 0;
}